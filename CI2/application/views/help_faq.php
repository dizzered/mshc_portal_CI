<h3 style="color:#b88f01; font-weight:bold">Clean Charges Only Statements</h3>
<p>You not only have the option to print a Detailed Statement which lists all transactions for a case, but you can also print a:<br />
<br />
<ul class="faq_ul_disk">
    <li><strong>Charges</strong> only statement which omits any payment and adjustment transactions on the account.</li>
    <li><strong>Summary</strong> statement which is a one page summary of the Medical, Physical Therapy, MRI or Baltimore Work Rehab activity for the case.</li>
</ul>
<br />
Simply select the desired output drop down option next to the Statement Link before printing the statement.</p>
<br />
<h3 style="color:#b88f01; font-weight:bold">User Maintenance</h3>
<p>The Confidentiality Agreement signed by your firm when requesting access to the MSHC Attorney Portal specified that each firm agrees to <strong>"Allow access to our web portal ONLY to personnel who have an essential need for access and request access user names and identification unique to each individual."</strong><br />
<br />
Each firm has a designated user with User Maintenance Rights. This individual has the ability to:<br />
<br />
<ul class="faq_ul_disk">
<li >Assist each user in the firm with changing their password.</li>
<li >Unlock an account after too many unsuccessful login attempts.</li>
<li >Update the email address associated with each account.</li>
<li >Add new users to the portal for new employees of the firm.</li>
<li >Delete accounts of staff who are no longer employed by the firm.</li>
</ul>
</p>
