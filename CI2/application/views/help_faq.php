<h3 style="color:#b88f01; font-weight:bold">Client Search</h3>
<p>When searching for your clients, less is more. Rarely is it necessary to enter multiple search criteria. This also increases the possibility of entering incorrect information, resulting in a "<i>No records to display</i>." message. The more criteria that you enter, the more limited the results will be, so <strong style="text-decoration:underline">here are some tips</strong> to help you find your clients more easily.<br />
<br  />
<ul class="faq_ul_disk">
	<li ><strong>Attorneys</strong>: If you have multiple attorneys in your firm, use the Select All button in case the client is linked to a different attorney than first thought.</li>
	<li ><strong>Name</strong>: Enter only the last name or even a portion of the last name. Usually this is enough to locate the desired client. For common last names enter last name < space > a portion of the first name. Do not use a comma separator between the last and first name.</li>
	<li ><strong>Account</strong>: Searching by our internal account number, if you have it handy, is one of the most specific methods for locating an account.</li>
	<li ><strong>SSN</strong>: When searching by social security number, enter only the last 4 digits. This is usually sufficient to locate the client.</li>
</ul>
</p>
