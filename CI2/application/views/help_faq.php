<h3 style="color:#b88f01; font-weight:bold">Mileage Reimbursement Form</h3>
<p>If you produce Mileage Reimbursement Forms for your clients, we have an option that will make it a little easier.<br/>
<br />
<ul class="faq_ul_numeric">
	<li ><span>Access the <strong>Appointment History</strong> screen for your client either by clicking on the <span style="text-decoration:underline">History</span> link from the Client Search screen or from the <span style="text-decoration:underline">Appointments</span> link on the Case <strong>Summary</strong> screen.</span></li>
	<li ><span>You will want to filter the list to only include kept appointments.</span></li>
    	<ul class="faq_ul_disk">
		<li >Enter the word "<i>Kept</i>" in the filter block under the <strong>Status</strong> column heading. If it is not displaying click the Refresh button.</li>
		<li >Click the filter options button to the right of the field and select <strong>Contains</strong>.</li>
		<li >This will display only the appointments with a status of "Kept".</li>
        </ul>
	<li ><span>Next Click the <strong>Export to Excel</strong> or <strong>Export to Word</strong> option. This will automatically open the selected program and you will have a list of all of the appointments that can be reformatted to serve as a mileage report.</span></li>
</ul>
</p>
<br />
<p><strong>Note</strong>: We do have a Mileage Report enhancement planned to include an option that would not only produce this report for you, but will automatically calculate and include the distance from your client's home or work address to the MSHC office where the patient was seen. We will notify you when this becomes available.</p>
